package com.mrvicari.splittingthebills.model;

/**
 * Different frequencies when Bills are paid
 */
public enum Period
{
    /**
     * Once every month
     */
    MONTHLY,

    /**
     * Once every three months
     */
    QUARTERLY,

    /**
     * Once every six months
     */
    BIANNUALLY,

    /**
     * Once every twelve months
     */
    ANNUALLY
}
